public class Powers {

    public static void main(String[] args) {
        double num = 16;
        System.out.println("Base Number: " + num);
        System.out.println("Square: " + Math.pow(num, 2));
        System.out.println("Cube: " + Math.pow(num, 3));
        System.out.println("4th Power: " + Math.pow(num, 4));
        System.out.println("5th Power: " + Math.pow(num, 5));
        System.out.println("Square Root: " + Math.sqrt(num));
        System.out.println("Cube Root: " + Math.cbrt(num));
    }
}
